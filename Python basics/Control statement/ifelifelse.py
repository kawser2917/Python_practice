num1=int(input("Enter your number : "))

if num1>=80:
    print("A+")
elif num1>=70 and num1<80:
    print("A")
elif num1>=60 and num1<70:
    print("A-")
elif num1>=50 and num1<60:
    print("B")
elif num1>=40 and num1<50:
    print("C")
else:
    print("Fail . try better for future")
