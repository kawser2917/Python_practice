a=[10,20,30,40,50]
print(a)

#insetion method

a.append(15) #it will add element at the last position
print(a)

a.insert(2,25) #it will add at position 2 the number 25
print(a)

#Removing method

a.pop() #this function will delete last element from the list 
print(a) 

a.pop(3) #This method will remove index 3 value from the list 
print(a)

a.remove(50) #this function will remove specific value 
print(a)

#Making empty list
a.clear()
print(a)


